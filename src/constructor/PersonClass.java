package constructor;

public class PersonClass {
	
	String name;
	String dateOfBirth;
	int sin;
	
	public PersonClass()
	{
		
	}
	// Constructors are used to initialize the class level variables
	//Constructor have the should have the same name as the class
	//no return type
	public PersonClass(String na,String dob,int sno )
	{
		name=na;
		dateOfBirth=dob;
		sin=sno;
		
		
		
	}
	

}
